export function generateVerilogStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const params = parameterList(specs);
  const ports = portLines(specs);
  const rst = resetSignal(specs);
  const behaviour = opt(specs, "functional_behaviour");

  const paramBlock =
    params.length > 0
      ? ` #(\n${params.map((p) => `  ${p}`).join(",\n")}\n)`
      : "";

  // Ensure last port line has no trailing comma
  const portBlock = ports
    .map((line, i) => {
      if (i === ports.length - 1) return line.replace(/,\s*$/, "");
      return line;
    })
    .join("\n");

  const resetCondition = rst.hasReset
    ? rst.activeLow
      ? `!${rst.name}`
      : rst.name
    : null;
  const sensitivity =
    rst.async && rst.hasReset
      ? `posedge clk or ${rst.activeLow ? "negedge" : "posedge"} ${rst.name}`
      : "posedge clk";

  let body: string;

  if (behaviour === "fsm_linear" || behaviour === "fsm_multi_branch") {
    const states =
      behaviour === "fsm_linear"
        ? ["IDLE", "ACTIVE", "DONE"]
        : ["IDLE", "SETUP", "READ", "WRITE", "ERROR"];
    const stateWidth = states.length > 4 ? 3 : 2;
    const stateEnc = states
      .map((s, i) => `  localparam S_${s} = ${stateWidth}'d${i};`)
      .join("\n");
    const stateReg = `  reg [${stateWidth - 1}:0] state, next_state;\n`;
    const resetBlock = resetCondition
      ? `    if (${resetCondition}) begin\n      state <= S_${states[0]};\n    end else begin\n      state <= next_state;\n    end`
      : `    state <= next_state;`;

    body = [
      stateEnc,
      "",
      stateReg,
      `  always @(${sensitivity}) begin`,
      resetBlock,
      "  end",
      "",
      "  always @(*) begin",
      "    next_state = state;",
      "    case (state)",
      ...states.map(
        (s) =>
          `      S_${s}: begin\n        // TODO: Implementation\n      end`,
      ),
      "      default: next_state = S_" + states[0] + ";",
      "    endcase",
      "  end",
    ].join("\n");
  } else if (behaviour === "shift_register") {
    const resetBlock = resetCondition
      ? `    if (${resetCondition}) begin\n      shift_reg <= {DATA_WIDTH{1'b0}};\n    end else begin\n      // TODO: Implementation — parallel load or shift\n      shift_reg <= {shift_reg[DATA_WIDTH-2:0], 1'b0};\n    end`
      : `    // TODO: Implementation — parallel load or shift\n    shift_reg <= {shift_reg[DATA_WIDTH-2:0], 1'b0};`;
    body = [
      "  reg [DATA_WIDTH-1:0] shift_reg;",
      "",
      `  always @(${sensitivity}) begin`,
      resetBlock,
      "  end",
    ].join("\n");
  } else if (behaviour === "pipeline") {
    const resetBlock = resetCondition
      ? `    if (${resetCondition}) begin\n      stage1_valid <= 1'b0;\n      stage2_valid <= 1'b0;\n    end else begin\n      // TODO: Implementation — pipeline advancement\n      stage1_valid <= valid;\n      stage2_valid <= stage1_valid;\n    end`
      : `    // TODO: Implementation — pipeline advancement\n    stage1_valid <= valid;\n    stage2_valid <= stage1_valid;`;
    body = [
      "  reg [DATA_WIDTH-1:0] stage1_data, stage2_data;",
      "  reg stage1_valid, stage2_valid;",
      "",
      `  always @(${sensitivity}) begin`,
      resetBlock,
      "  end",
    ].join("\n");
  } else if (behaviour === "counter") {
    const overflow = opt(specs, "edge_cases");
    const wrapLogic =
      overflow === "overflow_saturate"
        ? "      if (count == {DATA_WIDTH{1'b1}})\n        overflow <= 1'b1;\n      else\n        count <= count + 1'b1;"
        : "      count <= count + 1'b1; // wraps naturally";
    const resetBlock = resetCondition
      ? `    if (${resetCondition}) begin\n      count <= {DATA_WIDTH{1'b0}};\n    end else begin\n${wrapLogic}\n    end`
      : wrapLogic;
    const extraRegs =
      overflow === "overflow_saturate" ? "  reg overflow;\n" : "";
    body = [
      "  reg [DATA_WIDTH-1:0] count;",
      extraRegs,
      `  always @(${sensitivity}) begin`,
      resetBlock,
      "  end",
    ].join("\n");
  } else {
    body = [
      "  // TODO: Implementation",
      `  always @(${sensitivity}) begin`,
      resetCondition
        ? `    if (${resetCondition}) begin\n      // reset outputs\n    end else begin\n      // TODO: Implementation\n    end`
        : "    // TODO: Implementation",
      "  end",
    ].join("\n");
  }

  return [
    HEADER,
    `module ${moduleName}${paramBlock} (`,
    portBlock,
    ");",
    "",
    body,
    "",
    "endmodule",
    "",
  ].join("\n");
}

export function generateSVStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const params = parameterList(specs);
  const rst = resetSignal(specs);

  const paramNames = params.map((p) =>
    p.replace(/parameter\s+/, "").split("=")[0]!.trim(),
  );

  const paramBlockLines =
    params.length > 0
      ? ["#(", ...params.map((p, i) => `  ${p}${i < params.length - 1 ? "," : ""}`), ")"]
      : [];

  const instParamLines =
    params.length > 0
      ? paramNames.map(
          (n, i) => `    .${n}(${n})${i < paramNames.length - 1 ? "," : ""}`,
        )
      : [];

  const lines: string[] = [
    HEADER,
    `// SystemVerilog wrapper / verification harness for ${moduleName}`,
    "",
    `module ${moduleName}_sv`,
    ...paramBlockLines,
    "(",
    "  input  logic clk,",
  ];

  if (rst.hasReset) lines.push(`  input  logic ${rst.name},`);

  lines.push(
    "  input  logic [DATA_WIDTH-1:0] data_in,",
    "  output logic [DATA_WIDTH-1:0] data_out",
    ");",
    "",
    `  ${moduleName} #(`,
    ...instParamLines,
    "  ) u_dut (",
    "    .clk(clk),",
  );

  if (rst.hasReset) lines.push(`    .${rst.name}(${rst.name}),`);

  lines.push(
    "    .data_in(data_in),",
    "    .data_out(data_out)",
    "  );",
    "",
    "  // TODO: Add assertions and coverage",
    "",
    "endmodule",
    "",
  );

  return lines.join("\n");
}

export function generatePackageStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const params = parameterList(specs);

  return [
    HEADER,
    `package ${moduleName}_pkg;`,
    "",
    ...params.map((p) => {
      const parts = p.replace("parameter ", "").split("=");
      const name = parts[0]!.trim();
      const val = parts[1]!.trim();
      return `  localparam int ${name} = ${val};`;
    }),
    "",
    "  // Derived parameters",
    "  localparam int BYTE_COUNT = DATA_WIDTH / 8;",
    "",
    "  // Common type definitions",
    "  typedef logic [DATA_WIDTH-1:0] data_t;",
    "",
    "endpackage",
    "",
  ].join("\n");
}

export function generateTestbenchStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const period = clockPeriodNs(specs);
  const rst = resetSignal(specs);

  const assertReset = rst.hasReset
    ? rst.activeLow
      ? "    dut.rst_n.value = 0"
      : "    dut.rst.value = 1"
    : "    # No reset signal — skip";

  const deassertReset = rst.hasReset
    ? rst.activeLow
      ? "    dut.rst_n.value = 1"
      : "    dut.rst.value = 0"
    : "    pass";

  return [
    `# ${HEADER.replace("// ", "")}`,
    `# cocotb testbench for ${moduleName}`,
    "",
    "import cocotb",
    "from cocotb.clock import Clock",
    "from cocotb.triggers import RisingEdge, Timer",
    "",
    "",
    "@cocotb.test()",
    `async def test_${moduleName}_reset(dut):`,
    '    """Verify reset behaviour."""',
    `    clock = Clock(dut.clk, ${period}, units="ns")`,
    "    cocotb.start_soon(clock.start())",
    "",
    "    # Assert reset",
    assertReset,
    "    await RisingEdge(dut.clk)",
    "    await RisingEdge(dut.clk)",
    "",
    "    # De-assert reset",
    deassertReset,
    "    await RisingEdge(dut.clk)",
    "",
    "    # TODO: Add functional checks",
    `    dut._log.info("Reset test passed")`,
    "",
    "",
    "@cocotb.test()",
    `async def test_${moduleName}_basic(dut):`,
    '    """Basic functional test."""',
    `    clock = Clock(dut.clk, ${period}, units="ns")`,
    "    cocotb.start_soon(clock.start())",
    "",
    "    # De-assert reset",
    deassertReset,
    "    for _ in range(5):",
    "        await RisingEdge(dut.clk)",
    "",
    "    # TODO: Drive stimulus and check outputs",
    `    dut._log.info("Basic test passed")`,
    "",
  ].join("\n");
}

