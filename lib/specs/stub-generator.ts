// ---- helpers ---------------------------------------------------------------

const HEADER = "// Generated by EasyChip Playground — Q3 2026";

function opt(specs: Record<string, string>, catId: string): string {
  return specs[catId] ?? "";
}

function resetSignal(specs: Record<string, string>): {
  name: string;
  activeLow: boolean;
  async: boolean;
  hasReset: boolean;
} {
  const ck = opt(specs, "clock_reset");
  if (ck === "no_reset")
    return { name: "", activeLow: false, async: false, hasReset: false };
  const activeLow = ck !== "sync_active_high";
  const async = ck === "async_active_low" || ck === "dual_clock";
  const name = activeLow ? "rst_n" : "rst";
  return { name, activeLow, async, hasReset: true };
}

function parameterList(specs: Record<string, string>): string[] {
  const p = opt(specs, "parameters");
  const map: Record<string, string[]> = {
    std_8bit: ["parameter DATA_WIDTH = 8", "parameter ADDR_WIDTH = 16"],
    std_32bit: ["parameter DATA_WIDTH = 32", "parameter ADDR_WIDTH = 32"],
    buffered: ["parameter DATA_WIDTH = 8", "parameter DEPTH = 256"],
    serial: ["parameter BAUD_DIV = 868", "parameter DATA_WIDTH = 8"],
    multi_channel: [
      "parameter NUM_CHANNELS = 4",
      "parameter RESOLUTION = 12",
    ],
  };
  return map[p] ?? ["parameter DATA_WIDTH = 8"];
}

function portLines(specs: Record<string, string>): string[] {
  const iface = opt(specs, "interface_ports");
  const rst = resetSignal(specs);
  const common = ["  input  wire clk,"];
  if (rst.hasReset) common.push(`  input  wire ${rst.name},`);

  const portMap: Record<string, string[]> = {
    minimal: [
      "  input  wire [DATA_WIDTH-1:0] data_in,",
      "  output reg  [DATA_WIDTH-1:0] data_out,",
      "  input  wire valid,",
      "  output reg  ready",
    ],
    standard: [
      "  input  wire [7:0] data_in,",
      "  output reg  [7:0] data_out,",
      "  input  wire wr_en,",
      "  input  wire rd_en,",
      "  output reg  busy,",
      "  output reg  done",
    ],
    bus_interface: [
      "  // Write address channel",
      "  input  wire [ADDR_WIDTH-1:0] s_axi_awaddr,",
      "  input  wire                  s_axi_awvalid,",
      "  output reg                   s_axi_awready,",
      "  // Write data channel",
      "  input  wire [DATA_WIDTH-1:0] s_axi_wdata,",
      "  input  wire                  s_axi_wvalid,",
      "  output reg                   s_axi_wready,",
      "  // Write response channel",
      "  output reg  [1:0]            s_axi_bresp,",
      "  output reg                   s_axi_bvalid,",
      "  input  wire                  s_axi_bready,",
      "  // Read address channel",
      "  input  wire [ADDR_WIDTH-1:0] s_axi_araddr,",
      "  input  wire                  s_axi_arvalid,",
      "  output reg                   s_axi_arready,",
      "  // Read data channel",
      "  output reg  [DATA_WIDTH-1:0] s_axi_rdata,",
      "  output reg  [1:0]            s_axi_rresp,",
      "  output reg                   s_axi_rvalid,",
      "  input  wire                  s_axi_rready",
    ],
    streaming: [
      "  input  wire [DATA_WIDTH-1:0] s_axis_tdata,",
      "  input  wire                  s_axis_tvalid,",
      "  output reg                   s_axis_tready,",
      "  input  wire                  s_axis_tlast,",
      "  output reg  [DATA_WIDTH-1:0] m_axis_tdata,",
      "  output reg                   m_axis_tvalid,",
      "  input  wire                  m_axis_tready,",
      "  output reg                   m_axis_tlast",
    ],
    memory_mapped: [
      "  input  wire [ADDR_WIDTH-1:0] addr,",
      "  input  wire [DATA_WIDTH-1:0] wdata,",
      "  output reg  [DATA_WIDTH-1:0] rdata,",
      "  input  wire we,",
      "  input  wire re,",
      "  input  wire cs",
    ],
  };

  const selected = portMap[iface] ?? portMap.minimal!;
  return [...common, ...selected];
}

function clockPeriodNs(specs: Record<string, string>): number {
  const t = opt(specs, "timing");
  const map: Record<string, number> = {
    "50mhz": 20.0,
    "100mhz": 10.0,
    "200mhz": 5.0,
    "10mhz": 100.0,
    freq_agnostic: 10.0,
  };
  return map[t] ?? 10.0;
}

function freqLabel(specs: Record<string, string>): string {
  const t = opt(specs, "timing");
  const map: Record<string, string> = {
    "50mhz": "50 MHz",
    "100mhz": "100 MHz",
    "200mhz": "200 MHz",
    "10mhz": "10 MHz",
    freq_agnostic: "Frequency-agnostic",
  };
  return map[t] ?? "100 MHz";
}

