export function generateYosysScript(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const tech = opt(specs, "target_tech");
  let readLib: string;
  let synthCmd: string;

  switch (tech) {
    case "sky130":
      readLib = "read_liberty -lib sky130_fd_sc_hd__tt_025C_1v80.lib";
      synthCmd = "synth -top " + moduleName;
      break;
    case "gf180":
      readLib =
        "read_liberty -lib gf180mcu_fd_sc_mcu7t5v0__tt_025C_1v80.lib";
      synthCmd = "synth -top " + moduleName;
      break;
    case "xilinx_7series":
      readLib = "# Vivado flow — no liberty file needed";
      synthCmd = "synth_xilinx -top " + moduleName + " -family xc7";
      break;
    case "intel_cyclone_v":
      readLib = "# Quartus flow — no liberty file needed";
      synthCmd = "synth_intel_alm -top " + moduleName + " -family cyclonev";
      break;
    default:
      readLib = "# Technology-agnostic — no liberty file";
      synthCmd = "synth -top " + moduleName;
      break;
  }

  const isAsicTarget = tech === "sky130" || tech === "gf180";
  const libFile =
    tech === "sky130"
      ? "sky130_fd_sc_hd__tt_025C_1v80.lib"
      : "gf180mcu_fd_sc_mcu7t5v0__tt_025C_1v80.lib";

  const techMapping = isAsicTarget
    ? `dfflibmap -liberty ${libFile}\nabc -liberty ${libFile}`
    : "# No liberty-based mapping for this target";

  return [
    `# ${HEADER.replace("// ", "")}`,
    `# Yosys synthesis script for ${moduleName}`,
    "",
    `read_verilog ../src/${moduleName}.v`,
    readLib,
    "",
    "# Elaborate",
    "hierarchy -check -top " + moduleName,
    "",
    "# Synthesis",
    "proc; opt; fsm; opt; memory; opt",
    synthCmd,
    "",
    "# Technology mapping",
    techMapping,
    "",
    "# Clean up",
    "clean",
    "",
    "# Reports",
    "stat",
    `write_verilog -noattr ${moduleName}_synth.v`,
    "",
  ].join("\n");
}

export function generateSDCStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const period = clockPeriodNs(specs);
  const freq = freqLabel(specs);
  const clkReset = opt(specs, "clock_reset");
  const isDualClock = clkReset === "dual_clock";
  const rst = resetSignal(specs);

  const lines: string[] = [
    `# ${HEADER.replace("// ", "")}`,
    `# SDC timing constraints for ${moduleName}`,
    `# Target: ${freq}`,
    "",
    `create_clock -name clk -period ${period.toFixed(1)} [get_ports clk]`,
  ];

  if (isDualClock) {
    lines.push(
      `create_clock -name clk_b -period ${(period * 1.5).toFixed(1)} [get_ports clk_b]`,
      "",
      "# Clock domain crossing",
      "set_clock_groups -asynchronous -group {clk} -group {clk_b}",
    );
  }

  lines.push(
    "",
    "# Input / output delays",
    `set_input_delay  -clock clk ${(period * 0.2).toFixed(1)} [all_inputs]`,
    `set_output_delay -clock clk ${(period * 0.2).toFixed(1)} [all_outputs]`,
    "",
    "# False paths on reset",
    rst.hasReset
      ? `set_false_path -from [get_ports ${rst.name}]`
      : "# No reset signal — no false path needed",
    "",
  );

  return lines.join("\n");
}

export function generateSpecDoc(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const lines: string[] = [
    `# ${moduleName} — Specification`,
    "",
    "> Generated by EasyChip Playground — Q3 2026",
    "",
    "## Selected Specifications",
    "",
  ];

  for (const cat of SPEC_CATEGORIES) {
    const selectedId = specs[cat.id];
    const option = selectedId ? getOptionById(cat.id, selectedId) : undefined;
    lines.push(
      `### Spec ${cat.number}: ${cat.title}`,
      "",
      option
        ? `**${option.label}** — ${option.description}`
        : "_Not specified_",
      "",
    );
  }

  lines.push(
    "## Notes",
    "",
    "- All signals active-high unless otherwise noted.",
    "- Parameters can be overridden at instantiation.",
    "- See README.md for build and simulation instructions.",
    "",
  );

  return lines.join("\n");
}

