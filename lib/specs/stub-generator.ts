export function generateMemoryStub(
  specs: Record<string, string>,
  moduleName: string,
): string {
  const mem = opt(specs, "memory");
  const rst = resetSignal(specs);
  const sensitivity =
    rst.async && rst.hasReset
      ? `posedge clk or ${rst.activeLow ? "negedge" : "posedge"} ${rst.name}`
      : "posedge clk";
  const resetCond = rst.hasReset
    ? rst.activeLow
      ? `!${rst.name}`
      : rst.name
    : null;

  if (mem === "register_file") {
    const lines: string[] = [
      HEADER,
      `module ${moduleName}_regfile #(`,
      "  parameter DATA_WIDTH = 8,",
      "  parameter NUM_REGS   = 16,",
      "  parameter ADDR_BITS  = $clog2(NUM_REGS)",
      ") (",
      "  input  wire                  clk,",
    ];
    if (rst.hasReset)
      lines.push(`  input  wire                  ${rst.name},`);
    lines.push(
      "  input  wire [ADDR_BITS-1:0]  wr_addr,",
      "  input  wire [DATA_WIDTH-1:0] wr_data,",
      "  input  wire                  wr_en,",
      "  input  wire [ADDR_BITS-1:0]  rd_addr,",
      "  output reg  [DATA_WIDTH-1:0] rd_data",
      ");",
      "",
      "  reg [DATA_WIDTH-1:0] regs [0:NUM_REGS-1];",
      "",
      "  // Write port",
      `  always @(${sensitivity}) begin`,
    );
    if (resetCond) {
      lines.push(
        `    if (${resetCond}) begin`,
        "      // TODO: Optional reset of register file",
        "    end else if (wr_en) begin",
      );
    } else {
      lines.push("    if (wr_en) begin");
    }
    lines.push(
      "      regs[wr_addr] <= wr_data;",
      "    end",
      "  end",
      "",
      "  // Read port (asynchronous)",
      "  always @(*) begin",
      "    rd_data = regs[rd_addr];",
      "  end",
      "",
      "endmodule",
      "",
    );
    return lines.join("\n");
  }

  if (mem === "shift_reg") {
    const lines: string[] = [
      HEADER,
      `module ${moduleName}_shiftreg #(`,
      "  parameter WIDTH = 8",
      ") (",
      "  input  wire             clk,",
    ];
    if (rst.hasReset)
      lines.push(`  input  wire             ${rst.name},`);
    lines.push(
      "  input  wire             shift_en,",
      "  input  wire             load,",
      "  input  wire [WIDTH-1:0] par_in,",
      "  output wire             ser_out",
      ");",
      "",
      "  reg [WIDTH-1:0] sr;",
      "",
      `  always @(${sensitivity}) begin`,
    );
    if (resetCond) {
      lines.push(
        `    if (${resetCond})`,
        "      sr <= {WIDTH{1'b0}};",
        "    else if (load)",
      );
    } else {
      lines.push("    if (load)");
    }
    lines.push(
      "      sr <= par_in;",
      "    else if (shift_en)",
      "      sr <= {sr[WIDTH-2:0], 1'b0};",
      "  end",
      "",
      "  assign ser_out = sr[WIDTH-1];",
      "",
      "endmodule",
      "",
    );
    return lines.join("\n");
  }

  if (mem === "single_port_sram") {
    return [
      HEADER,
      `module ${moduleName}_sram #(`,
      "  parameter DATA_WIDTH = 8,",
      "  parameter DEPTH      = 256,",
      "  parameter ADDR_WIDTH = $clog2(DEPTH)",
      ") (",
      "  input  wire                  clk,",
      "  input  wire [ADDR_WIDTH-1:0] addr,",
      "  input  wire [DATA_WIDTH-1:0] wdata,",
      "  output reg  [DATA_WIDTH-1:0] rdata,",
      "  input  wire                  we",
      ");",
      "",
      "  reg [DATA_WIDTH-1:0] mem [0:DEPTH-1];",
      "",
      "  always @(posedge clk) begin",
      "    if (we)",
      "      mem[addr] <= wdata;",
      "    rdata <= mem[addr];",
      "  end",
      "",
      "endmodule",
      "",
    ].join("\n");
  }

  if (mem === "dual_port_sram") {
    return [
      HEADER,
      `module ${moduleName}_dpram #(`,
      "  parameter DATA_WIDTH = 8,",
      "  parameter DEPTH      = 256,",
      "  parameter ADDR_WIDTH = $clog2(DEPTH)",
      ") (",
      "  input  wire                  clk_a,",
      "  input  wire [ADDR_WIDTH-1:0] addr_a,",
      "  input  wire [DATA_WIDTH-1:0] wdata_a,",
      "  output reg  [DATA_WIDTH-1:0] rdata_a,",
      "  input  wire                  we_a,",
      "  input  wire                  clk_b,",
      "  input  wire [ADDR_WIDTH-1:0] addr_b,",
      "  input  wire [DATA_WIDTH-1:0] wdata_b,",
      "  output reg  [DATA_WIDTH-1:0] rdata_b,",
      "  input  wire                  we_b",
      ");",
      "",
      "  reg [DATA_WIDTH-1:0] mem [0:DEPTH-1];",
      "",
      "  // Port A",
      "  always @(posedge clk_a) begin",
      "    if (we_a)",
      "      mem[addr_a] <= wdata_a;",
      "    rdata_a <= mem[addr_a];",
      "  end",
      "",
      "  // Port B",
      "  always @(posedge clk_b) begin",
      "    if (we_b)",
      "      mem[addr_b] <= wdata_b;",
      "    rdata_b <= mem[addr_b];",
      "  end",
      "",
      "endmodule",
      "",
    ].join("\n");
  }

  // Fallback (no_storage)
  return [
    HEADER,
    `// No memory module required for ${moduleName} (pure combinational).`,
    "",
  ].join("\n");
}
